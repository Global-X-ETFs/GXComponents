export const BufferChartStory: StoryObj<typeof BufferChart> = {
  render: () => {
    const [selectedPeriod, setSelectedPeriod] = useState<number>(
      new Date(bufferChartMockData[bufferChartMockData.length - 1]?.date).getFullYear()
    );
    const [filteredData, setFilteredData] = useState<BufferChartDataPoint[]>([]);

    const years = useMemo(() => {
      const inceptionYear = new Date(bufferChartMockData?.[0]?.date).getFullYear();
      const currentYear = new Date().getFullYear();
      return Array.from(
        { length: currentYear - inceptionYear! + 1 },
        (_, i) => inceptionYear! + i
      );
    }, [bufferChartMockData]);


    const mappedData: BufferChartDataPoint[] = useMemo(() => {
      return bufferChartMockData.map(item => ({
        ...item,
        date: new Date(item.date),
      }));
    }, [bufferChartMockData]);
    useEffect(() => {
      const result = mappedData.filter(
        (item) => new Date(item.date).getFullYear() === selectedPeriod
      );
      setFilteredData(result);
    }, [selectedPeriod, mappedData]);
    
    return (
      <div className="max-w-[850px] p-8">
        <Select onValueChange={(value: string) => setSelectedPeriod(Number(value))}>
          <SelectLabel>Select Period</SelectLabel>
          <SelectTrigger>
            <SelectValue defaultValue={selectedPeriod.toString()}>{selectedPeriod}</SelectValue>
          </SelectTrigger>
          <SelectContent>
            {years.map(year => (
              <SelectItem key={year} value={year.toString()}>{year}</SelectItem>
            ))}
          </SelectContent>
        </Select>
      
        <BufferChart
          data={filteredData}
          cutoffs={{
            buffer_min: -34.5,
            buffer_max: -4.5,
            cap_min: 0,
            cap_max: 11.43,
          }}
          endDate={new Date(selectedPeriod, 11, 29)}  
        />
      </div>
    );
  },
};

